function centerElement(el) {
  const w = window.innerWidth;
  const h = window.innerHeight;
  const r = el.getBoundingClientRect();
  const left = Math.max(8, (w - r.width) / 2);
  const top = Math.max(8, (h - r.height) / 2);
  el.style.left = `${left}px`;
  el.style.top = `${top}px`;
}

function keepInViewport(el, left, top) {
  const r = el.getBoundingClientRect();
  const maxLeft = Math.max(8, window.innerWidth - r.width - 8);
  const maxTop = Math.max(8, window.innerHeight - r.height - 8);
  return {
    left: Math.min(Math.max(8, left), maxLeft),
    top: Math.min(Math.max(8, top), maxTop)
  };
}

function makeDraggable(target, handle) {
  if (!target || !handle) return;
  if (window.matchMedia('(max-width: 960px)').matches) return;
  let dragging = false;
  let offsetX = 0;
  let offsetY = 0;

  handle.addEventListener('pointerdown', (e) => {
    if (e.target.closest('.close-btn')) return;
    dragging = true;
    handle.classList.add('dragging');
    const r = target.getBoundingClientRect();
    offsetX = e.clientX - r.left;
    offsetY = e.clientY - r.top;
    target.style.zIndex = `${++state.z}`;
    handle.setPointerCapture(e.pointerId);
  });

  handle.addEventListener('pointermove', (e) => {
    if (!dragging) return;
    const pos = keepInViewport(target, e.clientX - offsetX, e.clientY - offsetY);
    target.style.left = `${pos.left}px`;
    target.style.top = `${pos.top}px`;
    target.style.transform = 'none';
  });

  const stop = (e) => {
    if (!dragging) return;
    dragging = false;
    handle.classList.remove('dragging');
    if (e.pointerId) handle.releasePointerCapture(e.pointerId);
  };

  handle.addEventListener('pointerup', stop);
  handle.addEventListener('pointercancel', stop);
}

function updateTopIcons() {
  const themeImg = $('#theme-icon');
  const sfxImg = $('#sfx-icon');
  if (themeImg) themeImg.src = img(state.theme === 'dark' ? 'dark_mode_light.webp' : 'dark_mode_dark.webp');
  if (sfxImg) {
    const key = state.sfx ? 'sfx_on' : 'sfx_off';
    const shade = state.theme === 'dark' ? '_light' : '_dark';
    sfxImg.src = img(`${key}${shade}.webp`);
  }
}

function applyTheme() {
  document.body.classList.toggle('light', state.theme === 'light');
  localStorage.setItem('theme', state.theme);
  updateTopIcons();
}

function playStarSfx() {
  if (!state.sfx) return;
  starAudio.currentTime = 0;
  starAudio.play().catch(() => {});
}

function syncModalState() {
  const hasOpen = $$('.modal.open').length > 0;
  document.body.classList.toggle('has-modal-open', hasOpen);
}

function playHoverSfx() {
  if (!state.sfx) return;
  hoverAudio.currentTime = 0;
  hoverAudio.play().catch(() => {});
}

function playFrogHoverSfx() {
  if (!state.sfx) return;
  frogHoverAudio.currentTime = 0;
  frogHoverAudio.play().catch(() => {});
}

function setupSkillHoverSound() {
  $$('#modal-work .skill-pill').forEach((el) => {
    el.addEventListener('mouseenter', playHoverSfx);
  });
}

function openModal(id) {
  $$('.modal').forEach((m) => m.classList.remove('open'));
  const overlay = $(`#modal-${id}`);
  if (!overlay) return;
  overlay.classList.add('open');
  const card = $('.modal-card', overlay);
  if (card && !card.dataset.positioned) {
    centerElement(card);
    card.dataset.positioned = '1';
  }
  if (card) card.style.zIndex = `${++state.z}`;
  syncModalState();
}

function closeModal(id) {
  const el = $(`#modal-${id}`);
  if (el) el.classList.remove('open');
  syncModalState();
}

function setupInteractions() {
  const home = $('.scene');
  const homeBar = $('.scene .window-titlebar');
  if (home && homeBar && !window.matchMedia('(max-width: 960px)').matches) {
    const r = home.getBoundingClientRect();
    home.style.left = `${r.left}px`;
    home.style.top = `${r.top}px`;
    home.style.transform = 'none';
    makeDraggable(home, homeBar);
  }

  $$('.modal-card').forEach((card) => {
    const bar = $('.window-titlebar', card);
    makeDraggable(card, bar);
  });

  $$('.icon-btn').forEach((btn) => {
    btn.addEventListener('click', () => openModal(btn.dataset.modal));
  });

  $$('.close-btn').forEach((btn) => {
    btn.addEventListener('click', () => closeModal(btn.dataset.close));
  });

  const star = $('#star-downloads');
  if (star) {
    star.addEventListener('mouseenter', playStarSfx);
  }

  const frog = $('.frog');
  if (frog) {
    frog.addEventListener('mouseenter', playFrogHoverSfx);
  }

  const themeToggle = $('#theme-toggle');
  if (themeToggle) {
    themeToggle.addEventListener('click', () => {
      state.theme = state.theme === 'dark' ? 'light' : 'dark';
      applyTheme();
    });
  }

  const sfxToggle = $('#sfx-toggle');
  if (sfxToggle) {
    sfxToggle.addEventListener('click', () => {
      state.sfx = !state.sfx;
      localStorage.setItem('sfx', state.sfx ? 'on' : 'off');
      updateTopIcons();
    });
  }

  const initial = document.body.dataset.open || '';
  if (initial) openModal(initial);
  else syncModalState();

  window.addEventListener('resize', () => {
    if (window.matchMedia('(max-width: 960px)').matches) return;
    $$('.modal.open .modal-card').forEach((card) => {
      const r = card.getBoundingClientRect();
      const pos = keepInViewport(card, r.left, r.top);
      card.style.left = `${pos.left}px`;
      card.style.top = `${pos.top}px`;
    });
  });
}

applyTheme();
